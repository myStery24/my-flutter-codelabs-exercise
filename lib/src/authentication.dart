class ResetPwdForm extends StatefulWidget {
  @override
  ResetPwdFormState createState() => ResetPwdFormState();
}

class ResetPwdFormState extends State<ResetPwdForm> {
  TextEditingController forgotEmailController = TextEditingController();
  GlobalKey<FormState> _formKey = GlobalKey<FormState>();
  late String emailAddress;

  @override
  void initState() {
    super.initState();
    init();
  }

  Future<void> init() async {
    //
  }

  @override
  void setState(fn) {
    if (mounted) super.setState(fn);
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      resizeToAvoidBottomInset: true,
      appBar: AppBar(
        title: Text('Forgot Password'),
        centerTitle: true,
      ),
      body: SingleChildScrollView(
        child: Form(
          autovalidateMode: AutovalidateMode.onUserInteraction,
          key: _formKey,
          child: Container(
            padding: EdgeInsets.all(16),
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                SizedBox(height: 15.0),
                Text('Forgot your password ?',
                    style: TextStyle(
                      fontSize: 22,
                      fontWeight: FontWeight.bold,
                    )),
                SizedBox(height: 15.0),
                Text(
                    'Please enter your email address. We will send you an email to reset your password.',
                    style: TextStyle(fontSize: 14)),
                SizedBox(height: 20),
                TextFieldContainer(
                  child: TextFormField(
                    keyboardType: TextInputType.emailAddress,
                    onSaved: (newValue) => emailAddress = newValue!,
                    onChanged: (value) {
                      setState(() {
                        emailAddress = value;
                      });
                    },
                    validator: (value) {
                      if (value!.isEmpty) {
                        return 'Email address is required';
                      }
                      if (!RegExp(
                              r"[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?")
                          .hasMatch(value)) {
                        return 'Invalid email address';
                      }
                      return null;
                    },
                    decoration: InputDecoration(
                      icon: Icon(
                        Icons.email_outlined,
                        color: Colors.black,
                      ),
                      labelText: "Email Address",
                      hintText: "Enter your email",
                      border: InputBorder.none,
                      floatingLabelBehavior: FloatingLabelBehavior.always,
                    ),
                  ),
                ),
                SizedBox(height: 20.0),
                Row(
                  mainAxisAlignment: MainAxisAlignment.end,
                  children: [
                    StyledButton(
                        child: Text('RESET PASSWORD'),
                        onPressed: () async {
                          if (_formKey.currentState!.validate()) {
                            _formKey.currentState!.save();
                            await FirebaseAuth.instance
                                .sendPasswordResetEmail(email: emailAddress)
                                .then((value) {
                              showDialog(
                                context: context,
                                barrierDismissible:
                                    false, // users must tap button
                                builder: (_) => AlertDialog(
                                  title: Text("Reset Password"),
                                  content: Text(
                                      "We have sent you an email with a link to reset your password. Please check your email."),
                                  actions: <Widget>[
                                    TextButton(
                                      child: Text("Dismiss"),
                                      onPressed: () {
                                        Navigator.of(context).pop();
                                      },
                                    ),
                                  ],
                                  elevation: 24.0,
                                ),
                              );
                              throw ('Reset password link has been sent to your email');
                            }).catchError((error) {
                              throw error.toString();
                            });
                          }
                          ;
                        }),
                  ],
                ),
              ],
            ),
          ),
        ),
      ),
    );
  }
}

